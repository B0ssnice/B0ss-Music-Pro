const { EmbedBuilder } = require('discord.js');

module.exports = {
  name: "support",
  description: "Find out support information.",
  permissions: "0x0000000000000800",
  options: [],
  run: async (client, interaction) => {
    try {

      const discordLink = "https://discord.com/users/636615832218239016";
      const githubLink = "https://github.com/itsb0ss";
      const youtubeLink = "https://youtube.com/@_itsb0ss";
        const embed = new EmbedBuilder()
            .setColor('#b300ff')
            .setAuthor({
              name: 'Support Server',
              iconURL: 'https://cdn.discordapp.com/avatars/636615832218239016/a_789b35ae1b2101b0e79ae4397cde14d5.gif?size=1024', 
              url: 'https://discord.gg/clcrp'
          })
            .setDescription(`➡️ Contact B0ss - ${discordLink}\n\n➡️ **Follow Me:**\n- GitHub - ${githubLink}\n- YouTube - ${youtubeLink}`)
            .setImage('https://cdn.discordapp.com/attachments/1029606495261827223/1274737509070540800/togif.gif')
            .setTimestamp();

      return interaction.reply({ embeds: [embed] });
    } catch (e) {
    console.error(e); 
  }
  },
};

